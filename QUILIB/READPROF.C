/*
 *	readProfile - read built-in ASQ profile
 */

int readProfile( int first, char *line, int nline )
{
	static int index = 0;
	char *p;

	if (first) {
		index = 0;
	}

	p = ProfileText[index];
	if (!p) return FALSE;

	strncpy(line,p,nline-1);
	line[nline-1] = '\0';
	index++;
	return TRUE;
}
